void main() {
  String day = 'Monday';
  switch (day) {
    case 'Monday':
      print('Start of the work week');
      break;
    case 'Wednesday':
      print('Midweek day');
      break;
    case 'Friday':
      print('End of the work week');
      break;
    default:
      print('Just another day');
  }
}
